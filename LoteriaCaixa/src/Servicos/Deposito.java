package Servicos;

public class Deposito {
/** Classe objeto comum, para guardar os valores do deposito.*/
    
/** Atributos da classe. */
private int conta;
private double valor;
/* Get's e set's. */

    public double getConta() {
        return conta;
    }

    public void setConta(int conta) {
        this.conta = conta;
    }

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

}
